/** 元；损益类字段（正负有意义）。 */
const y = (raw: number | null | undefined) =>
  formatRawAsNumeric({ raw, unit: "yuan", sign_aware: true });

/** 元；规模/市值类（展示通常不带符号前缀）。 */
const yScale = (raw: number | null | undefined) =>
  formatRawAsNumeric({ raw, unit: "yuan", sign_aware: false });

/** 百分点/久期等：组件侧直接读 ``raw`` 与 ``toFixed`` 拼接展示，不用 ``pct`` 的 ×100 语义。 */
const rp = (raw: number | null | undefined, sign_aware = false) =>
  formatRawAsNumeric({ raw, unit: "ratio", sign_aware });

const rps = (raw: number | null | undefined) =>
  formatRawAsNumeric({ raw, unit: "ratio", sign_aware: true });

const bpv = (raw: number | null | undefined) =>
  formatRawAsNumeric({ raw, unit: "bp", sign_aware: true });

const dv = (raw: number | null | undefined) =>
  formatRawAsNumeric({ raw, unit: "dv01", sign_aware: false });

